// While Loop
let count = 0
while (count <= 10) {
    console.log(count);
    count+=2 // count = count+2
}

let myArray = ["Uzair", "Mahrus", "Mudassir"]
let arr = 0
while(arr < myArray.length){
    console.log(myArray[arr]);
    arr++
}

// do-while Loop
let score = 11
do{
    console.log(`Score is ${score}`);
    score++
}while(score <= 10);
// Even if the condition doesn't match, the statement will be printed once